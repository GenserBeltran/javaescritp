let a = 3, b = 2, y = 4, x = 5;
let z = a + b;
console.log(z);

z = a - b;
console.log(z);

z = a * b;
console.log(z);

z = a / b;
console.log(z);

z = a % b;
console.log(z);

z = a ** b;
console.log(z);

/*      --- Incremeto ---   */

//Preincremento (el operador ++ antes de la variable)
z = ++a;
console.log(z)
console.log(a);

//Post-incremento (el operador ++ despues de la varable)

z = b++;
console.log(z);
console.log(b);

/*      --- Decremeto ---   */

//PreDecremeto (el operador -- antes de la variable)
z = --y;
console.log(z)
console.log(y);

//Post-Decremeto (el operador -- despues de la varable)
z = x--;
console.log(z);
console.log(x);